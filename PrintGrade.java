package uno.career;

public class PrintGrade {

	public static void main(String[] args) {
		int a=55;
		if(a<60) {
			System.out.println("F");	
		}
		if(a>=60&&a<=74) {
			System.out.println("C");
		}
		if(a>=75&&a<=89) {
			System.out.println("B");
		}
		if(a>=90&&a<=100) {
			System.out.println("A");
		}
	}
	

}
